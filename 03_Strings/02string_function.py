# Escape sequence characters in Python

print("Escape sequence characters in Python are")

print("Hello \n Goodmorning") # \n used for next line
print("Hello \t Goodmorning") # \t used for tab space 
print("Hello \"Goodmorning\" ") #\"String"\ used to print inside " quotes
print('Hello \'Goodmorning\' ') # \'String\' used to print inside ' quotes
print("Hello \\ Goodmorning") # \\ used to print backslash
