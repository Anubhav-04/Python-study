# creation of virtal environment

# -----------pip install virtualenv -- install the virtual environment package
#------------virtualenv newenv(name of environment) create a virtual environment
#------------source newenv/bin/activate ----- To activate the virtual environment
#------------chmod +x newenv/bin/activate ----- (If permission required)
#------------deactivate ----- To deactivate and come out of virtual environment
#------------pip freeze ----- To view the list of pacakages installed
#------------pip freeze > requirements.txt ----- To write all the installed packages in the a file
#------------pip install -r requirements.txt --- To install all the packages simultaneously
